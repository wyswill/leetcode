function longestCommonPrefix(strs) {
  let comPre = "", first = strs.shift();
  if (!first) return comPre;
  for (let i = 0; i < first.length; i++) {
    let flag = strs.every(ele => ele[i] === first[i]);
    if (flag) comPre += first[i]; else return comPre;
  }
  return comPre;
}

